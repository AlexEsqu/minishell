#include "minishell.h"

int	pwd(int fdout)
{
	if (getenv("PWD") == NULL || fdout < 0)
		return (-1);
	ft_putstr_fd(getenv("PWD"), fdout);
	write(fdout, "\n", 1);
	return (0);
}
